import { gql } from "@apollo/client";

// 댓글 가져오기
export const FETCH_USEDITEM_QUESTIONS = gql`
  query fetchUseditemQuestions($page: Int, $useditemId: ID!) {
    fetchUseditemQuestions(page: $page, useditemId: $useditemId) {
      _id
      contents
      useditem {
        _id
      }
      user {
        _id
        name
        picture
      }
      createdAt
    }
  }
`;

